package redis.dataStructure;

public class DataStructure {
/**
 * 1.String:可以是字符串，整数，或者是浮点数
 * 		对整个字符串或者字符串其中一部分操作，对整数或者浮点数执行自增或者自减
 * 2.List：一个链表，链表上的每个节点都包含了一个字符串
 * 		从链表两端推入或者弹出元素，根据偏移量对链表进行修剪trim，读取单个或多个元素，根据值查找或者移除元素
 * 3.Set:包含字符的无序收集器，并且被包含的数据都是独一无二的	。
 * 		添加、获取、移除单个元素，检查一个元素是否存在于集合中，计算交集、并集／差集，从集合中随机获取元素
 * 4.Hash：包含键值对的无序散列表
 * 		添加获取移除单个键值对，获取所有键值对
 * 5.ZSet：字符串成员与浮点数分值之的有序映射，元素排列顺序由分值大小决定
 * 		添加、获取、删除单个元素，根据分值范围或者成员来获取元素。
 */
}
